-- SAP HANA Database Query to verify the organizational mapping
SELECT 
    T001.BUKRS AS Company_Code,
    T001W.WERKS AS Plant_ID,
    TVKO.VKORG AS Sales_Organization
FROM T001
LEFT JOIN T001K ON T001.BUKRS = T001K.BUKRS
LEFT JOIN T001W ON T001K.BWKEY = T001W.BWKEY
LEFT JOIN TVKO ON T001.BUKRS = TVKO.BUKRS
WHERE T001.BUKRS = 'NC10';
